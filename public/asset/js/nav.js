document.addEventListener("DOMContentLoaded", function () {
    const dropdownContainer = document.querySelector(".nav-dropdown");
    const dropdownContent = dropdownContainer.querySelector(".dropdown-nav-content");

    dropdownContainer.addEventListener("mouseenter", function () {
        dropdownContent.style.display = "block";
    });

    dropdownContainer.addEventListener("mouseleave", function () {
        dropdownContent.style.display = "none";
    });
});


document.addEventListener("DOMContentLoaded", function () {
    const dropdownContainer = document.querySelector(".cat-dropdown");
    const dropdownContent =
        dropdownContainer.querySelector(".dropdown-content");
    var dropdownItems = document.querySelectorAll(".dropdown-item");

    dropdownContainer.addEventListener("mouseenter", function () {
        dropdownContent.style.display = "block";
    });

    dropdownContainer.addEventListener("mouseleave", function () {
        dropdownContent.style.display = "none";
    });

    dropdownItems.forEach(function (item) {
        item.addEventListener("mouseover", function () {
            this.querySelector(".in-dropdown-content").style.display = "block";
        });

        item.addEventListener("mouseout", function () {
            this.querySelector(".in-dropdown-content").style.display = "none";
        });
    });
});


